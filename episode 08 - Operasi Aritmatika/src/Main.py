a = 10
b = 3

# operasi penjumlahan
hasil = a + b
print(a," + ", b, "= ", hasil)

# operasi pengurangan
hasil = a - b
print(a," - ", b, "= ", hasil)

# operasi perkalian
hasil = a * b
print(a," x ", b, "= ", hasil)

# operasi pembagian
hasil = a / b
print(a," / ", b, "= ", hasil)

# operasi eksponen (pangkat)
hasil = a ** b
print(a," ** ", b, "= ", hasil)